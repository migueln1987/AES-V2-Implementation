def ShiftRows(state):
    newList = []
    lst1 = []
    lst2 = []
    lst3 = []
    lst4 = []
    for i in range(len(state)):
        if i%4 == 0:
            lst1.append(state[i])
        if i%4 == 1:
            if i == 1:
                lst2.insert(3, state[i])
            else:
                lst2.insert(-1, state[i])
        if i%4 == 2:
            if i == 10:
                lst3.insert(0, state[i])
            else:
                lst3.insert(1, state[i])
        if i%4 == 3:
            if i == 15:
                lst4.insert(0, state[i])
            else:
                lst4.insert(2, state[i])
